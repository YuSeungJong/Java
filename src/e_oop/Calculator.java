package e_oop;

public class Calculator {

	// +
	double plus(double x, double y) {
		return x + y;
	}
	
	// -
	double subtraction(double x, double y) {
		return x - y;
	}
	
	// *
	double multi(double x, double y) {
		return x * y;
	}

	// /
	double division(double x, double y) {
		return x / y;
	}

	// %
	double remainder(double x, double y) {
		return x % y;
	}
}
